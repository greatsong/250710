# 메인 애플리케이션
def main():
    # 타이틀과 인트로
    st.title("🌍 MBTI별 맞춤 여행지 추천 ✈️")
    st.markdown("### 당신의 성격 유형에 완벽하게 맞는 여행지를 찾아보세요! 🎯")
    
    # MBTI 선택
    mbti_types = list(travel_data.keys())
    selected_mbti = st.selectbox(
        "당신의 MBTI를 선택해주세요 😊",
        mbti_types,
        index=None,
        placeholder="MBTI를 선택하세요..."
    )
    
    # MBTI가 선택되었을 때
    if selected_mbti:
        # 풍선 효과
        st.balloons()
        
        # 선택된 MBTI 표시
        st.success(f"🎉 {selected_mbti} 유형이 선택되었습니다!")
        
        # 데이터 가져오기
        destinations = travel_data[selected_mbti]["destinations"]
        
        # 각 여행지별로 표시
        for i, destination in enumerate(destinations, 1):
            with st.container():
                st.markdown("---")
                
                # 여행지 이름
                st.header(f"{i}. {destination['name']}")
                
                # 관광지 정보
                st.markdown("#### 🏛️ 꼭 가봐야 할 관광지")
                
                for attraction in destination['attractions']:
                    parts = attraction.split(' - ')
                    attraction_name = parts[0]
                    reason = parts[1] if len(parts) > 1 else ""
                    
                    with st.expander(f"📍 {attraction_name}"):
                        st.write(f"**추천 이유:** {reason}")
                
                # 간격 추가
                st.markdown("<br>", unsafe_allow_html=True)
        
        # 마무리 메시지
        st.markdown("---")
        st.markdown("### 🎒 즐거운 여행 되세요!")
        st.markdown("각 여행지에서 당신만의 특별한 경험을 만들어보세요. ✨")
        
        # 추가 풍선 효과 버튼
        if st.button("🎉 여행 계획 완료!"):
            st.balloons()
            time.sleep(1)
            st.success("완벽한 여행 계획이 세워졌네요! 안전하고 즐거운 여행 되세요! 🌟")

if __name__ == "__main__":
    main()
